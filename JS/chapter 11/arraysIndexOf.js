//.IndexOf to find index of an item in an array
var dogs = ["Frida", "Ailo", "Max", "Sam", "Tanja", "Kaisa"]
//returns the first index at which a given element can be found
console.log(dogs.indexOf("Max")); //2
console.log(dogs.indexOf("Kaisa")); //5
//returns -1 if element is not present.
console.log(dogs.indexOf("Frodo")) //-1


